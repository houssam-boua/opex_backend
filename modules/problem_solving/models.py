class QRQC(BaseModel):
    """
    Fiche QRQC issue du front-end React.
    """
    class Urgency(models.TextChoices):
        CRITIQUE = "critique", "Critique"
        HAUTE    = "haute",    "Haute"
        MOYENNE  = "moyenne",  "Moyenne"
        BASSE    = "basse",    "Basse"

    class Status(models.TextChoices):
        NOUVEAU  = "nouveau",  "Nouveau"
        EN_COURS = "en_cours", "En cours"
        RESOLU   = "resolu",   "Résolu"
        CLOTURE  = "cloture",  "Clôturé"

    title    = models.CharField(max_length=255)
    problem  = models.TextField(blank=True)
    poste    = models.CharField(max_length=255, blank=True)
    urgency  = models.CharField(max_length=20, choices=Urgency.choices, default=Urgency.MOYENNE)
    status   = models.CharField(max_length=20, choices=Status.choices, default=Status.NOUVEAU)
    cause    = models.TextField(blank=True, help_text="Cause racine identifiée")

    objects = TenantManager()

    class Meta:
        db_table = "problem_solving_qrqc"
        ordering = ["-created_at"]

    def __str__(self):
        return f"QRQC: {self.title} ({self.get_status_display()})"


class QRQCAction(BaseModel):
    """
    Actions rapides pour le QRQC.
    Syncs to shared.models.Action.
    """
    class Status(models.TextChoices):
        A_FAIRE  = "a_faire",  "À faire"
        EN_COURS = "en_cours", "En cours"
        TERMINE  = "termine",  "Terminé"

    qrqc        = models.ForeignKey(QRQC, on_delete=models.CASCADE, related_name="actions")
    description = models.TextField()
    assigned_to = models.ForeignKey("accounts.Employee", on_delete=models.SET_NULL, null=True, blank=True)
    due_date    = models.DateField(null=True, blank=True)
    status      = models.CharField(max_length=20, choices=Status.choices, default=Status.A_FAIRE)

    objects = TenantManager()

    class Meta:
        db_table = "problem_solving_qrqc_actions"

    def sync_to_shared_action(self):
        from shared.models import Action
        status_map = {
            "a_faire": "open",
            "en_cours": "in_progress",
            "termine": "done"
        }
        priority_map = {
            "critique": "critical",
            "haute": "high",
            "moyenne": "medium",
            "basse": "low"
        }
        action, _ = Action.objects.update_or_create(
            reference_id=self.id,
            module_source="qrqc",
            tenant=self.tenant,
            defaults={
                "title": f"[QRQC] {self.qrqc.title}",
                "description": self.description,
                "priority": priority_map.get(self.qrqc.urgency, "medium"),
                "status": status_map.get(self.status, "open"),
                "assigned_to": self.assigned_to,
                "due_date": self.due_date,
                "created_by": self.created_by,
                "action_type": "qrqc_action",
            }
        )
        return action
